--CLIAM INTIMATION CLIENT PORTAL 

select distinct LC.PLC_LOCADESC,count(CR_ID) as Branch_Wise_Claims_Intimate from hicl_changerequest CR
left outer join PR_GN_LC_LOCATION LC on LC.PLC_LOCACODE=CR.LOC_CODE
inner  join  HICL_USERs SU on Upper(CR.USERID)=Upper(SU.USERID)

where to_char(TO_DATE(CR_DATE, 'DD-MON-YYYY hh24:mi:ss'),'DD-MON-YYYY') between TO_DATE('01-JAN-2019') and TO_DATE('22-Apr-2019')
    AND    CR.BUSINESS_CLASS='NonCBD'  
        and 
         CR.CR_ID like 'CI%'
          group by LC.PLC_LOCADESC,CR.LOC_CODE;


